import UIKit

public extension UIColor {
    static var appWhite: UIColor { return UIColor(red: 245.0/255.0, green: 245.0/255.0, blue: 245.0/255.0, alpha: 1.0) }
    static var appBlack: UIColor { return UIColor(red: 30.0/255.0, green: 30.0/255.0, blue: 30.0/255.0, alpha: 1.0) }
    static var iOS7Blue: UIColor { return UIColor(red: 0.0, green: 122.0/255.0, blue: 1.0, alpha: 1.0) }
    static var veryLightGray: UIColor { return UIColor(white: 0.99, alpha: 1) }
    static var evenColor: UIColor { return UIColor.appWhite }
    static var oddColor: UIColor { return UIColor(hex: "e0e0e0") }

}
